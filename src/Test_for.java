
public class Test_for {

	public static void main(String[] args) {
		for(int i = 2;i<=9;i++) {
			for(int j = 1;j<=9;j++) {
				System.out.println(i+" * "+j+" = "+i*j);
			}
			
		}
		int i = 10;
		while(i>=0) {
			System.out.println(i--);
		}
		i = 10;
		while(i>=0) {
			System.out.println(i--);
		}
		for(int i1 = 10;i1>=0;i1--) {
			System.out.println(i1);
		}
	}

}
